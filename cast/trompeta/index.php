		<p class="titulo">4. CUIDADO DE LAS BOMBAS</p>

		<img src="trp5.gif" style="float:right; margin-left:20px" alt="" />
		<p>La grasa de bombas mantiene las bombas herm&eacute;ticas y un movimiento suave. Este es un factor esencial en el cuidado de su instrumento. Para aplicar grasa de bombas:</p>
		<ol>
			<li>Quite la bomba presionando el pist&oacute;n correspondiente. (Refi&eacute;rase al principio de este manual para ver los diagramas que muestran las bombas y sus pistones correspondientes.)</li>
			<li>Inserte una esquina de un pa&ntilde;o de algod&oacute;n limpio a trav&eacute;s del agujero de la varilla de limpiar (YAC 1666P). Enrolle el pa&ntilde;o alrededor de la varilla, <b>asegur&aacute;ndose de cubrirla entera.</b> Limpie cualquier suciedad del interior y exterior de la bomba.</li>
			<li>Limpie cualquier suciedad o la materia extra&ntilde;a de la superficie de las bombas interiores. Aplique una peque&ntilde;a cantidad de grasa de bombas.</li>
			<li>Introduzca y saque la bomba repetidas veces, asegur&aacute;ndose de que la grasa quede extendida uniformemente.</li>
		</ol>
		  
		<p><i>* El aceite se recomienda para las bombas de afinaci&oacute;n de trompetas y cornetas.</i></p>
		<br /><br style="clear:both" />

		<p class="titulo">5. CUIDADO DEL INTERIOR DEL INSTRUMENTO</p>

		<img src="trp6.gif" style="float:right; margin-left:20px" alt="" />
		<p class="titulo2">Vaciar todos los l&iacute;quidos.</p>
		<ol>
			<li>Vac&iacute;e cualquier l&iacute;quido del interior de los tubos.</li>
			<li>Vac&iacute;e cualquier l&iacute;quido del interior de la bomba de afinaci&oacute;n.</li>
			<li>Abra todas las llaves de desagüe.</li>
		</ol>
		<br />
 
		<p class="titulo2">Desmontar el instrumento.</p>
		<p>Sobre un pa&ntilde;o limpio en una superficie lisa, desmonte su instrumento completamente: la boquilla, las tres bombas, y los tres pistones deber&iacute;an ser quitados.<br />
		<b>No desmontar los pistones.</b></p>
		<br style="clear:both" />

		<p class="titulo2">Limpieza de tuber&iacute;as.</p>
		<img src="trp7a.gif" style="float:right; margin-left:20px" alt="" />
		<ol>
			<li>Mezcle una soluci&oacute;n de jab&oacute;n en agua templada, no caliente.</li>
			<li>Sumerja el instrumento completamente, usando una peque&ntilde;a sonda flexible para limpiar cada parte.</li>
			<li>Aclarar a fondo con agua limpia y templada. Tenga sobre todo cuidado en no da&ntilde;ar la superficie del pist&oacute;n o el interior de la camisa.</li>
			<li>Secar a fondo, quitando el agua pasando un pa&ntilde;o limpio.</li>
		</ol>
		<img src="trp7c.gif" style="float:right; margin-left:20px" alt="" />
		<br />
		<p><i>* Puede limpiar tambiar tanbi&eacute;n los pistones y la boquilla, aprobechando el mismo jabon. Si estos est&aacute;n muy sucios puede dejarlos en remojo 5 minutos.</i></p>
		<br />

		<p class="titulo">6. CUIDADO DEL EXTERIOR DE LA TROMPETA</p>
		<p>Para el cuidado diario, use un pa&ntilde;o de pulir para quitar el polvo y las huellas digitales. 
		La suciedad se quita f&aacute;cilmente con la laca de pulir (sobre instrumentos con acabado de laca). Los instrumentos tambi&eacute;n se pueden pulir usando un pa&ntilde;o especialmente tratado. 
		Cuando se use cera liquida para plata, hay que tener cuidado de no darla en las partes moviles.</p>
		<p><i>* Nota: Use cera para plata s&oacute;lo sobre instrumentos plateados. </i></p>
		<br /><br />

		<p class="titulo">7. DONDE GUARDAR LA TROMPETA</p>
		
		<p>Guardar siempre el instrumento en la caja despues de tocar y para su transporte. Asegurese que la boquilla y otros accesorios quedan fuertemente asegurados, Puesto que pueden rayar y abollar su instrumento si vian flojos durante el transporte. No ponga nada en la caja que podr&iacute;a hacer presi&oacute;n sobre el instrumento.</p>
