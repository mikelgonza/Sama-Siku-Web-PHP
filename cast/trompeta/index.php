		<p class="titulo2">Limpieza de los pistones.</p>

		<img src="trp4.gif" style="float:right; margin-left:20px" alt="" />
		<ol>
			<li>Saque los pistones en una línea recta, - no los gire. Deje los pistones sobre una superficie limpia.</li>
			<li>Use un cepillo de pistones para limpiar el interior de la camisa. Insertelo y girelo para quitar toda la suciedad.<br />
			<i>* En lugar de un cepillo, puede usarse una varilla de limpieza envuelta con una tela de algod&oacute;n.</i></li>
			<li>Limpie cualquier suciedad o la material extra&ntilde;o de la superficie exterior de los pistones con un pa&ntilde;o limpio.</li>
			<li>Introduzca con cuidado los pistones en las camisas y aplique 2-3 gotas de aceite. No gire el pist&oacute;n dentro de la camisa. Ins&eacute;rtelo directamente, alineando la gu&iacute;a del pist&oacute;n con la ranura de la gu&iacute;a interior de la camisa. Aseg&uacute;rese que cada pist&oacute;n es introducido en su camisa correspondiente.</li> 
			<li>Sustituya y apriete gorros. Mueva los pistones arriba y abajo para asegurarse que el aceite cubra toda su superficie.</li> 
		</ol>
