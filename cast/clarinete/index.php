		<p>Los clarinetes de madera son m&aacute;s sensibles que el pl&aacute;stico a cambios de clima. Los cambios bruscos de temperatura y/o de humedad causan peque&ntilde;os cambios en estos instrumentos, causando principalmente rajas en el cuerpo o que se cedan las llaves. Se recomienda el siguiente cuidado para alargar y mantener la calidad de su clarinete.</p>

		<p><i>* Tenga mucho cuidado en evitar la exposici&oacute;n a cambios bruscos de temperatura y humedad. Evite la exposici&oacute;n directa a:</i></p>
		<ul>
			<li>Luz del sol</li>
			<li>Calentadores o Acondicionadores de aire</li>
			<li>Lluvia</li>
		</ul>

		<p><i>* Quite TODA la HUMEDAD despu&eacute;s de tocar. Refi&eacute;rase a  la Secci&oacute;n 4 en el Cuidado del Interior para las instrucciones detalladas.</i></p>
		
		<br /><br />
		
		<p class="titulo">1. MONTANDO SU CLARINETE</p>
	  
		<img src="clarinete.gif" style="float:right; margin-left:20px" alt="" />
		<p><i>Monte, afine, limpie, y lubrique su instrumento la primera vez para bajo la supervisi&oacute;n de su maestro.</i></p>

		<p class="titulo2">Aplicando vaselina para el corcho</p>

		<p>La vaselina aplicada a los corchos antes del montaje facilita un ajuste apropiado de las juntas del clarinete. Una completa y uniforme capa en todas las juntas de corcho le proporciona protecci&oacute;n y extiende la vida del corcho.</p>

		<p class="titulo2">Montaje Requinto</p>
		<ol>
			<li>Una el barrilete y la campana al cuerpo principal.</li>
			<li>Una la boquilla al barrilete.</li>
		</ol>

		<p class="titulo2">Montaje Clarinete</p>
		<img src="puente.gif" style="float:right; margin-left:20px" alt="" />
		<ol>
			<li>Una el barrilete al cuerpo superior y la campana al cuerpo inferior.</li>
			<li>Junte el cuerpo superior y el inferior con un suave giro. Preste atenci&oacute;n a las llaves del puente; estas suelen da&ntilde;arse f&aacute;cilmente por un montaje descuidado. Para alinearlos, sostenga ligeramente las llaves de la parte superior, asegur&aacute;ndose que la llave del puente esta arriba. Encaje llave del puente de la parte superior encima de la llave del puente de la parte de abajo.</li>
		</ol>

		<p class="titulo2">Montaje Clarinete Bajo y alto</p>
		<ol>
			<li>Si es posible, una la parte superior e inferior. Si no, proceda a la siguiente instrucci&oacute;n.</li>
			<li>Una el tudel con la parte de la campana.</li>
			<li>Una la boquilla al tudel. Tenga cuidado de no presionar el tudel hacia abajo.</li>
		</ol>
		
		<p class="titulo2">Colocaci&oacute;n de la Ca&ntilde;a sobre la Boquilla</p>

		<img src="boquilla.gif" alt="" style="float:right; margin-left:20px" />
		<p>Con cuidado sostenga la ca&ntilde;a en su boca durante unos minutos antes de ponerla sobre la boquilla. Coloque la ca&ntilde;a sobre la boquilla y aseg&uacute;rela con la abrazadera. La posici&oacute;n normal es donde puede ver solamente la punta de la boquilla sobre la ca&ntilde;a.</p>
		<br />
		<br />

		<p class="titulo">2. AFINACION</p>

		<img src="tudel.gif" alt="" style="float:right; margin-left:20px" />
		<p>La afinaci&oacute;n se suele ver afectada por la temperatura, as&iacute; que caliente al instrumento antes de tocar soplando aire a trav&eacute;s de &eacute;l. Para afinarlo, saque el barrilete en clarinetes Sib y requintos y la boquilla en clarinetes altos y bajos. Cuanto hay que sacar depende de la preferencia individual y m&eacute;todo de tocar.</p>
		<br />
		<br />

		<p class="titulo">3. CUIDADO DEL INTERIOR DEL CLARINETE</p>

		<p class="titulo2">Quitar la Ca&ntilde;a</p>

		<p>Quite la ca&ntilde;a de la boquilla soltando la abrazadera y desliz&aacute;ndola fuera de esta. Seque la ca&ntilde;a con una tela limpia y p&oacute;ngala en su funda.</p>

		<p class="titulo2">Quitar la Humedad de la Boquilla.</p>

		<img src="limpieza.gif" alt="" style="float:right; margin-left:20px" />
		<p>Quite toda la humedad y suciedad de dentro de la boquilla pasando un trapo de limpieza a trav&eacute;s de &eacute;l. Tenga cuidado de no rascar la boquilla, y siempre pase el trapo desde el lado del corcho. Cuando la boquilla esta especialmente sucia puede usarse un cepillo de boquilla de instrumentos de viento madera.</p>

		<p class="titulo2">Limpieza del Cuerpo y zapatas.</p>
		<ol>
			<li>Pase el trapo de limpieza a trav&eacute;s del cuerpo insert&aacute;ndolo por la campana y sac&aacute;ndolo por el barrilete para quitar toda la humedad de los clarinetes. Para los clarinetes altos y bajos, desmonte seque separadamente las partes.</li>
			<li>Los corchos tienden a coger la humedad, sobre todo en las uniones superiores e inferiores. Los clarinetes de madera empezar&aacute;n a rajarse en este &aacute;rea, hay que tener cuidado de quitar toda la humedad con una gasa.</li>
			<li>Para secar una zapata, insertar un papel absorbente entre esta y el agujero, presione ligeramente hacia abajo y aparte el papel cuidadosamente.</li>
		</ol>
		<br />
		<br />

		<p class="titulo">4. CUIDADO DEL EXTERIOR DEL CLARINETE</p>

		<p>Para el cuidado diario, limpie todo el cuerpo del clarinete con un trapo de pulir, teniendo cuidado de no presionar ni forzar las llaves. Limpie bajo las llaves y tenga cuidado de no da&ntilde;ar las zapatas.</p>

		<p class="titulo2">Lubricando m&aacute;stiles claves</p>

		<p>El aceite debe ser aplicado entre los ejes aproximadamente cada dos meses. Use s&oacute;lo una muy peque&ntilde;a cantidad y evite que caiga sobre las zapatas. Si es necesario, quite cualquier exceso con un pa&ntilde;o absorbente.</p>

		<p class="titulo2">Los agujeros</p>

		<p>Use un limpiador de agujeros o un bastoncillo para limpiar el espacio de debajo y de entre los agujeros.</p>

		<br />
		<br />

		<p class="titulo">5. GUARDAR EL INSTRUMENTO</p>

		<p>El caso siempre deber&iacute;a ser usado para almacenar el instrumento despu&eacute;s de tocar y para el transporte. Aseg&uacute;rese de quitar la ca&ntilde;a y guardarla correctamente en su funda o en un portaca&ntilde;as. No ponga nada en caja que pueda hacer presi&oacute;n sobre el instrumento.</p><br />
