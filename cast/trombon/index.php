		<p class="titulo">3. LIMPIEZA DE LA VARA</p><br />

		<p>Para preservar y alargar la vida del tromb&oacute;n hay que procurar mantener la vara limpia, y secarla despu&eacute;s de tocar, puesto que al solidificarse la saliva, puede llegar a entorpecer la salida del aire. Tambi&eacute;n hay que procurar lubricar (sin pasarse) de vez en cuando la vara interior con la exterior, manteni&eacute;ndola as&iacute; en optimas condiciones:</p>

		<img src="bone4.gif" alt="" style="float:right; margin-left:20px" />
		<p class="titulo2">Lubricaci&oacute;n diaria de la vara</p>

		<ol>
			<li>Saque con cuidado la vara externa y p&oacute;ngala sobre una superficie limpia y plana.</li>
			<li>Aplicar una peque&ntilde;a cantidad de crema de varas a la parte que es ligeramente m&aacute;s gruesa de la vara interna. Extienda uniformemente, hasta que se cubra de una fina capa. No aplique demasiada crema pues impedir&aacute; el movimiento de la vara.</li>
			<li>Usar una botella de roc&iacute;o para rociar el agua uniformemente sobre las varas interiores.</li>
			<li>Montar la vara externa con la interna, movi&eacute;ndola hacia adelante y hacia atr&aacute;s para extender uniformemente el lubricante.</li>
		</ol>

		<p><i>* Puede usarse crema l&iacute;quida en lugar de la crema de varas y agua, aplic&aacute;ndose de la misma forma</i></p>
		
		<br /><br />

		<img src="bone8.gif" alt="" style="float:right; margin-left:20px" />
		<p class="titulo2">Limpieza de la vara</p>
		<ol>
			<li>Mezclar una soluci&oacute;n de jab&oacute;n y agua tibia (no caliente).</li>
			<li>Colocar la vara entera en la soluci&oacute;n.</li>
			<li>Usar una sonda grande y flexible para limpiar la superficie interior de las varas internas y externas.</li>
			<li>Limpiar las superficies externas con un pa&ntilde;o suave y limpio.</li>
			<li>Aclarar a fondo con agua tibia (no caliente) y limpia.</li>
			<li>Secar a fondo, quitando toda el agua. Para secar el interior usar una varilla de limpieza  envuelta completamente con un pa&ntilde;o largo y seco - asegur&aacute;ndose que ninguna parte met&aacute;lica queda descubierta. Hay que tener mucho cuidado de no golpear la punta de la varilla con la parte curvada.</li>
		</ol>

