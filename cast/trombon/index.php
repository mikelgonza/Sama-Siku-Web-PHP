		<p class="titulo">4. CUIDADO DEL INTERIOR DEL TROMBON</p>
		<br />
		<p class="titulo2">Lubricando la bomba de afinaci&oacute;n</p>
		<p>La grasa de bombas mantiene las bombas herm&eacute;ticas y con un movimiento suave. Esto es un factor esencial en el cuidado de su tromb&oacute;n. Para aplicar grasa de bombas:</p>
		<ol>
			<li>Quitar cada una de las bombas y l&iacute;mpielas de suciedad y sustancias extra&ntilde;as.</li>
			<li>Aplicar una fina capa de grasa a la superficie interior de las bombas.</li>
			<li>Introduzca la bomba, haciendo que la grasa se extienda uniformemente.</li>
		</ol>

		<br />

		<p class="titulo2">Limpieza de la secci&oacute;n de la campana</p>
		<ol>
			<li>Quitar la bomba de afinaci&oacute;n.</li>
			<li>Utilizar la misma soluci&oacute;n de jab&oacute;n y agua usada anteriormente, sumergiendo la campana y la bomba de afinaci&oacute;n.</li>
			<li>Limpiar, aclarar y secar de la misma forma que la vara.</li>
		</ol>

		<p><i>* Las secciones del transpositor s&oacute;lo deber&aacute;n ser limpiadas por un t&eacute;cnico competente durante la revisi&oacute;n del instrumento.</i></p>

		<br />

		<img src="bone9.gif" alt="" style="float:right; margin-left:20px" />
		<p class="titulo2">Limpieza de la boquilla</p>
		<ol>
			<li>Preparar una soluci&oacute;n de jab&oacute;n como en la secci&oacute;n anterior.</li>
			<li>Sumergir la boquilla y usando un cepillo de boquilla, limpie el interior y exterior de esta.</li>
			<li>Aclarar a fondo con agua limpia y tibia (no caliente).</li>
		</ol>

		<p><i>* Si la boquilla esta demasiado sucia, dejarla 5 minutos en remojo y luego limpiarla con el cepillo.</i></p>

		<br />

		<p class="titulo">5. CUIDADO DEL TRANSPOSITOR (si lo incluye)</p>
		<br />
		<img src="bone6.gif" alt="" style="float:right; margin-left:20px" />
		<p class="titulo2">Engrase del transpositor</p>
		<p>El transpositor debe ser engrasado antes del montaje del tromb&oacute;n.</p>
		<ol>
			<li>Quitar vara y colocarla sobre una superficie limpia y lisa.</li>
			<li>Sostenga la parte de la campana con esta mirando hacia arriba.</li>
			<li>Aplicar unas gotas de aceite de rotor en el receptor de la vara.</li>
			<li>Bajar la palanca para girar el cilindro, extendiendo el aceite uniformemente sobre el rotor y la cubierta.</li>
			<li>Aplicar unas gotas de aceite de directamente al tallo de rotor.</li>
			<li>Con cuidado quite la tapa del cilindro y aplique unas gotas de aceite a los cojinetes. Gire el cilindro y ponga la tapa.</li>
			<li>Aplicar una gota de aceite a cada uni&oacute;n de la palanca con el cuerpo.</li>
		</ol>

		<p><i>* No saque el cilindro transpositor. Esto deber&aacute; hacerse s&oacute;lo por un t&eacute;cnico de reparaci&oacute;n competente durante la revisi&oacute;n del instrumento.</i></p>

		<br />

		<p class="titulo2">Encordando el transpositor</p>

		<p><i>* Nota: Use s&oacute;lo la cuerda dise&ntilde;ada expresamente para transpositores.</i></p>

		<p>Necesitar&aacute; un peque&ntilde;o destornillador, tijeras y la cuerda para volver a encordar.Por favor siga estas direcciones con cuidado:</p>
		<img src="transpositor.gif" alt="" style="float:right; margin-left:20px" />
		<ol>
			<li>Ponga la secci&oacute;n de la campana del tromb&oacute;n, con la campana apuntando hacia usted. El transpositor debe aparecer como muestra la foto.</li>
			<li>Afloje los tornillos (C) y (E) con destornillador peque&ntilde;o. Corte aproximadamente un trozo de cord&oacute;n de  22 cm de largo y ate un nudo en un extremo. Empiece enhebrando el cord&oacute;n a trav&eacute;s del agujero del fondo de la barra (A), desde el exterior hacia el cilindro, hasta que el nudo haga tope.</li>
			<li>Con el brazo de parada (B) a la derecha de la barra (A), lleve el cord&oacute;n alrededor de la cima del tallo del rotor en el sentido de las agujas del reloj, entonces doble el cord&oacute;n completamente alrededor del tornillo peque&ntilde;o (C) en el sentido contrario a las agujas del reloj.</li>
			<li>Contin&uacute;e alrededor del fondo del brazo de parada (B) al agujero (D) cerca de la cabeza de la barra (A). Pase el cord&oacute;n a trav&eacute;s de este agujero y d&oacute;blelo bajo la cabeza del tornillo peque&ntilde;o (E) de la barra en el sentido de las agujas del reloj.</li>
			<li>Ahora tense bastante el cord&oacute;n, y apriete fuerte el tornillo (E) para sostener la palanca en lugar.</li>
			<li>Posicione la palanca para que la barra del cord&oacute;n sea paralela a la cima de la cubierta del rotor.</li>
			<li>Apriete el tornillo del cord&oacute;n (C).</li>
		</ol>
		
